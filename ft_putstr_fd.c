#include "ft_printf.h"

int	ft_putstr_fd(char *s, int fd)
{
	int	i;

	if (s == NULL)
	{
		write(1, "(null)", 6);
		return (6);
	}
	i = 0;
	while (s[i])
		write (fd, &s[i++], 1);
	return (i);
}
